                    <div class="level2">关于做好P2P网络借贷风险专项整治整改验收工作的通知（附全文）</div>
                    <div class="level2">【发文部门】P2P网贷风险专项整治工作领导小组办公室</div>
                    <div class="level2">【发文日期】2017年12月13日</div>
                    <div class="level2">【发文字号】网贷整治办函【2017】57号</div>
                    <div class="text">
                        <p>解读</p>
                        <p>备受业内关注的P2P风险专项整治工作，终于将进入整改验收阶段。</p>
                        <p>12月13日，P2P网贷风险专项整治工作领导小组办公室于近日向各地P2P整治联合工作办公室下发了《关于做好P2P网络借贷风险专项整治整改验收工作的通知》（57号文）（下称“通知”），对下一步的整改验收阶段做出了具体、详细的部署。</p>
                        <p>通知要求，各地应在2018年4月底前完成辖内主要P2P机构的备案登记工作、6月底之前全部完成；并对债权转让、风险备付金、资金存管等关键性问题作出进一步的解释说明。</p>
                        <p>通知指出，各地整治办应指定官方网站对拟备案网贷机构的整改验收情况进行公示，公示时间不得少于两周。</p>
                        <p>对于各类P2P平台，通知提出五项原则：</p>
                        <p>一、验收合格的P2P平台，予以备案登记，确保其正常经营；</p>
                        <p>二、积极配合整改验收工作但最终没有通过的平台，可据具体情况，或引导其逐步清退业务、退出市场，或整合相关部门及资源，采取市场化方式，进行并购重组；</p>
                        <p>三、对于严重不配合整改验收工作，违法违规行为严重甚至已有经侦介入或已失联的平台，应由相关部门予以取缔；</p>
                        <p>四、对于逃避整改验收，暂停自身业务或不处于正常经营状态的机构，需予以高度重视，此类机构恢复经营后也需酌情予以备案；</p>
                        <p>五、对于行业中业务余额较大、影响较大、跨区域经营的机构，由机构注册地整治办建立联合核查机制，向机构业务发生地征求相关意见。</p>
                        <p>值得注意的是，通知明确指出，对于2016年8月24日后新设的P2P平台，本次网贷整治期间原则上不予备案登记。此外，对于自始未纳入本次整治的各类机构，在整改验收期间提出备案申请的，各地整治办不得予以进行验收及备案登记；对于《P2P管理暂行办法》规定的十三项禁止性行为及单一借款人上线的规定，P2P自2016年8月24日不再违反，相应存量业务未化解完成的P2P不得备案登记；对于开展过涉及首付贷、校园贷及现金贷等业务的P2P，应暂停新业务，逐步压缩存量，否则不予备案。</p>
                        <p>另外，在《通知》的附件部分，对整改验收过程中部分具体问题进行了解释说明：</p>
                        <p>1．债权转让</p>
                        <p>是否合规，应当具体问题具体分析。</p>
                        <p>（1）合规</p>
                        <p>为解决流动性的低频转让。</p>
                        <p>（2）违规</p>
                        <p>开展类ABS或以打包资产、证券化资产、信托资产、基金份额等形式的债权转让行为；</p>
                        <p>由网贷机构高管/关联人与借款人签订借款合同直接放款，再根据借款金额在平台放标，将债权转让给实际出借人的“超级放款人”模式的债权转让；</p>
                        <p>以活期、定期理财产品的形式对接债权转让标的。</p>
                        <p>以出借人所持债权作为抵（质）押，提供贷款。</p>
                        <p>2．风险备付金</p>
                        <p>禁止辖内机构继续提取、新增风险备付金。</p>
                        <p>已提取的风险备付金应逐步消化，压缩规模。</p>
                        <p>禁止网贷机构以风险备付金进行宣传。</p>
                        <p>各地应积极引导网贷机构引入第三方担保等保障方式。</p>
                        <p>3．资金存管</p>
                        <p>网贷机构应当与通过测评的银行业金融机构开展资金存管业务合作。</p>
                        <p>4．综合借款成本及“现金贷”</p>
                        <p>网贷机构应依照141号文要求开展业务；</p>
                        <p>（变相）撮合高利贷的网贷机构不予备案登记。</p>
                        <p>5．法人及分支机构备案</p>
                        <p>申请备案登记的网贷机构应当为法人机构；</p>
                        <p>申请登记时报送本法人机构所有分支机构信息。</p>
                        <p>6．线下营销</p>
                        <p>对于大规模线下营销的网贷机构：</p>
                        <p>应消减淘汰/转型线下营销门店及人员；</p>
                        <p>清理、摘除相关标示、标脾、宣传牌、宣传单等；</p>
                        <p>不得再在电子渠道以外的物理场所宣传/推介/项目。</p>
                        <p>7．网贷机构业务规模控制</p>
                        <p>网贷机构当自整改通知书下发之日起，实现：</p>
                        <p>存量违规业务持续下降；</p>
                        <p>不再新增任何违规业务。</p>
                        <p>违反《办法》十三项禁止性行为以及单一借款人上限的网贷机构，在违规业务未U化解完成前不予备案登记。</p>
                        <p>8．网贷机构与地方金融交易所</p>
                        <p>网贷机构应停止与各类地方金融交易所合作。</p>
                        <p>存量合作业务的转让或清偿应于本次专项整治结束前完成。</p>
                        <p>这是首次明确网贷平台全面停止与各类地方金融交易所的合作。</p>
                        <p>此前整治办64号文要求互联网金融平台停止和地方各类交易场所的违法违规业务，是要求地方各类交易场所不得将权益拆分为均等份额公开发行、不得超过200人投资者上限等，但并未直接提出网贷平台全面停止与地方各类交易场所的合作。</p>
                        <p>9．网贷机构业务外包及机构分立</p>
                        <p>不得外包核心业务。</p>
                        <p>因业务分割分立出的实体：视为原网贷机构组成部分，一并验收管理。</p>
                        <p>10．网贷机构信息披露</p>
                        <p>整治结束前，实现严格按《披露指引》进行信息披露。</p>
                        <p>所有分支机构信息报送至本地区网贷整治办。</p>
                        <p>11．网贷机构基础设施</p>
                        <p>整治结束前提升安防和开发能力至满足IT安全要求。</p>
