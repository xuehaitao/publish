                    <div class="text">
                        <p>各省（区、市、计划单列市）网络借贷风险专项整治联合工作办公室：</p>
                        <p>为扎实推进P2P网络借贷（以下简称“网贷”）风险专项整治工作，在分类处置工作基础上，进一步加强各省（区、市、计划单列市）辖内网贷机构的整改验收工作，根据《网络借贷信息中介机构业务活动管理暂行办法》（2016年第1号主席令，以下简称《办法》）、《关于印发《P2P网络借贷风险专项整治工作实施方案>的通知》(银监发(2016)11号）和相关工作部署精神，现将网贷风险专项整治整改验收工作有关要求及安排通知如下：</p>
                        <p>一、充分认识整改验收的重要意义</p>
                        <p>整改验收是本次网贷风险专项整治工作的关键核心环节，各省（区、市、计划单列市）网络借贷风险专项整治联合工作办公室（以下简称各地整治办）应当高度重视整改验收相关工作，加强统筹，强化责任，做好整改验收与机构备案的衔接，科学把握备案机构数量和质量，按照“明确标准、严格把关、积极稳妥”的原则，一家一策、整改验收合格一家、备案一家，有序开展辖内存量网贷机构的整改验收与备案登记工作，实现行业市场出清、扶优抑劣、规范纠偏，确保向常态化监管的稳步过渡，真正引导行业守住法律底线和政策红线，回归信息中介本质，坚持小额分散功能，定位线上经营模式，建立合理定价机制，以服务实体经济和小微企业。</p>
                        <p>二、扎实做好整改验收的各项工作</p>
                        <p>（一）成立验收专班，落实各方责任</p>
                        <p>各省（区、市、计划单列市）人民政府应当高度重视本次整改验收工作，提高认识，切实落实属地管理职责，做好组织管理和风险预案，成立由省（区、市、计划单列市）金融办、银监局以及人民银行分支机构、公安、通信管理、工商管理等部门组成的联合整改验收小组，进行交叉核验，统筹考虑并确定验收标准和措施。</p>
                        <p>各地整治办应当切实落实部门责任，加强对整改验收工作的组织协调，充分利用各职能部门及第三方专业机构的力量做好整改验收工作。</p>
                        <p>各地整治办应指定官方网站对拟备案网贷机构的整改验收情况进行公示，公示时间应不少于两周，并要求网贷机构在自身官方网站及APP上及时对本机构整改验收及备案登记情况进行信息披露。</p>
                        <p>整改验收公示期间，各地整治办如收到异地整治办、出借人或借款人以及其他网贷机构对公示机构的举报，经核查属实的，各地整治办应当撤销公示内容并对网贷机构重新进行整改验收。</p>
                        <p>最终的整改验收合格证明文件应当由本省（区、市、计划单列市）金融办、银监局的负责同志共同签发。</p>
                        <p>（二）严格验收标准，确保分类施策</p>
                        <p>各地整治办应当对辖内机构进行全覆盖、有重点的实质检查，可以通过核查账务系统、资金流水、融资项目真实性、抽查借贷合同、暗访检查违规线下营销和违规宣传行为、产品合规性调查等手段，查实查透网贷机构存在的问题，严防被检查机构“带病”通过验收。</p>
                        <p>对于不同情况的网贷机构，应当分类施策、科学处置：</p>
                        <p>一是对于验收合格的网贷机构，应当尽快予以备案登记，确保其正常经营；</p>
                        <p>二是对于积极配合整改验收工作但最终没有通过的机构，可以根据其具体情况，或引导其逐步清退业务、退出市场，或整合相关部门及资源，采取市场化方式，进行并购重组；</p>
                        <p>三是对于严重不配合整改验收工作，违法违规行为严重，甚至已经有经侦介入或已经失联的机构，应当由相失部门依据《非法金融机构和非法金融业务活动取缔办法》等相关法律法规予以取缔；</p>
                        <p>四是对于为逃避整改验收，暂停自身业务或不处于正常经营状态的机构，各地整治办要予以高度重视，要求此类机构恢复正常经营后，酌情予以备案；</p>
                        <p>五是对于行业中业务余额较大、影响较大、跨区域经营的机构，由机构注册地整治办建立联合核查机制，向机构业务发生地整治办征求相关意见。</p>
                        <p>各省（区、市、计划单列市）要结合本地区各部门、各机构的实际情况，积极稳妥推进相关工作，充分协调工商管理、公安等具有行政执法权的部门在机构退出环节依法履行相应职能，确保不发生处置风险的风险，守住不发生系统性区域性风险和不发生大规模群体事件的底线。</p>
                        <p>（三）明确时间节点，严格政策界限</p>
                        <p>各省（区、市、计划单列市）对于辖内机构的具体整改验收，应当明确不同的时间节点，分类加以规制，具体包括：</p>
                        <p>一是根据互联网金融风险专项整治领导小组有关要求，对于在《办法》发布之日（2016年8月24日）后新设立的网贷机构或新从事网络借贷业务的网贷机构，在本次网贷风险专项整治期间，原则上不予备案登记；</p>
                        <p>二是对于自始末纳入本次网贷专项整治的各类机构，在整改验收期间提出备案登记申请的，各地整治办不得对此类机构进行整改验收及备案登记；</p>
                        <p>三是对于《办法》规定的十三项禁止性行为及单一借款人借款上限规定，网贷机构应当自2016年8月24日后不再违反，相应存量业务没有化解完成的网贷机构不得进行备案登记；</p>
                        <p>四是对于开展过涉及房地产首付贷、校园贷以及现金贷等业务的网贷机构，应当按照《关于进一步加强校园贷规范管理工作的通知》（银监发[2017]26号）、《关于对“现金贷”业务进行规范整顿通知》（整治办函[2017]141号）的要求，暂停新增业务，对存量业务逐步压缩，制定退出时间表，对于相关监管要求下发后继续违规发放以上三类业务的机构不予备案；</p>
                        <p>五是辖内各网贷机构应当与通过网贷专项整治领导小组办公室组织开展的网贷资金存管业务测评的银行业金融机构开展资金存管业务合作；</p>
                        <p>六是对于在规定时间内没有通过本次整改验收，无法完成备案登记但依然实质从事网贷业务的机构，各省（区、市、计划单列市）应当协调相应职能部门予以处置，包括注销其电信经营许可、封禁网站，要求金融机构不得向其提供各类金融服务等。</p>
                        <p>（四）把握工作进度，逐步完成备案</p>
                        <p>请各省（区、市、计划单列市）处理好工作力度和节奏的关系，严格遵守最新的互联网金融风险专项整治大的时间框架，分阶段完成整改验收以及后续备案登记工作：</p>
                        <p>1、2018年4月底之前完成辖内主要网贷机构的备案登记工作；</p>
                        <p>2、对于违规存量业务较多，难以及时完成处置的部分网贷机构，应当于2018年5月底之前完成相应业务的处置、剥离以及备案登记工作；</p>
                        <p>3、对于难度极大、情况极其复杂的个别机构，最迟应当于2018年6月末之前完成相关工作。</p>
