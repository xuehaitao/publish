                    <div class="text">
                        <p>关于整改验收过程中部分具体问题的解释说明</p>
                        <p>1．关于债权转让有关问题。</p>
                        <p>对于债权转让是否合规，应当具体问题具体分析。为解决流动性问题，在出借人之间进行的低频次债权转让，应认定为合规；对于开展类资产证券化业务或实现以打包资产、证券化资产、信托资产、基金份额等形式的债权转让行为则应该认定为违规；对于由网贷机构高管或关联人根据机构的授权，与借款人签订借款合同，直接放款给借款人，再根据借款金额在平台放标，将债权转让给实际出借人的“超级放款人”模式的债权转让，由于其可能导致网贷机构虚构标的、将项目拆分期限错配、直接或间接归集出借人资金等行为，应当认定为违规；以活期、定期理财产品的形式对接债权转让标的，由于可能造成资金和资产的期限错配，应当认定为违规。同时，各网贷机构不得以出借人所持债权作为抵（质）押，提供贷款。</p>
                        <p>2．关于风险备付金有关问题。</p>
                        <p>目前市场上部分机构出于解决信用风险的考虑，提取了部分风险备付金，这一经营模式与网贷机构的信息中介定位不符。应当禁止辖内机构继续提取、新增风险备付金，对于已经提取的风险备付金，应当逐步消化，压缩风险备付金规模。同时严格禁止网贷机构以风险备付金进行宣传。各地应当积极引导网贷机构采取引入第三方担保等他方式对出借人进行保障。</p>
                        <p>3．关于资金存管有关问题。</p>
                        <p>网贷专项整治领导小组办公室委托中国互联网金融协会开展网贷资金存管业务测评，测评工作将按照“标准统一、质量优先、客观公正、实事求是”的原则，严格依据《网络借贷资金存管指引》有序开展。网贷机构应当与通过测评的银行业金融机构开展资金存管业务合作。</p>
                        <p>4．关于综合借款成本及“现金贷”有关问题。</p>
                        <p>各地应当继续做好对“现金贷”的清理整顿工作，要求辖内网贷机构依照《关于对“现金贷”业务进行规范整顿通知》相关要求开展业务，对于继续撮合或变相撮合违反法律有关利率规定的借贷业务的网贷机构不予备案登记。</p>
                        <p>5．关于法人及分支机构备案有关问题。</p>
                        <p>申请备案登记的网贷机构应当为法人机构，在申请登记的同时，应当将本法人机构的所有分支机构信息报送至本地区网贷整治办公室，同时，相关整治办公室应当及时共享相关信息，并密切配合，共同处置相关风险。</p>
                        <p>6．关于线下经营的有关问题。</p>
                        <p>对于大规模从事线下营销的网贷机构，应当消减淘汰或转型线下营销门店及人员，清理、摘除相关标示、标脾、宣传牌、宣传单等，不得再在互联网、固定电话、移动电话等电子渠道以外的物理场所进行宣传或推介融资项目。</p>
                        <p>7．关于网贷机构业务规模控制有关问题。</p>
                        <p>网贷机构应当持续优化自身业务结构，调控自身业务规模，在前述要求的基础上，应当自整改通知书下发之日起，实现存量违规业务</p>
                        <p>持续下降，确保不再新增任何违规业务。对于存在违反《办法》规定的十三项禁止性行为以及单一借款人上限的网贷机构，在其相应违规业务没有化解完成前，各省（区、市、计划单列市）整治办应当不予备案登记。</p>
                        <p>8．关于网贷机构与地方金融交易所合作有关问题。</p>
                        <p>对于与各类地方金融交易所进行合作的网贷机构，应当停止合作，存量合作业务逐步转让或清偿，最终于本次专项整治结束之前完成。</p>
                        <p>9．关于网贷机构业务外包及机构分立有关问题。</p>
                        <p>辖内网贷机构不得将核心业务进行外包。对于将自身业务分割，将原有网贷机构分立为不同实体的情况，如果其分立出的实体，只与将其分立出的网贷机构进行业务合作的，则应当将分立后的机构视为原网贷机构的组成部分，进行一并验收管理。</p>
                        <p>10．关于网贷机构信息披露有关问题。</p>
                        <p>网贷机构应该继续完善自身信息披露，于自身官方网站或APP上确实披露项目风险及资金投向，同时将本法人机构的所有分支机构信息报送至本地区网贷整治办公室，在本次专项整治结束前，网贷机构应当依据银监会发布的《网络借贷信息中介机构业务活动信息披露指引》进行完整的信息披露。</p>
                        <p>11．关于网贷机构基础设施有关问题。</p>
                        <p>对于缺乏合规的网络安全设施的网贷机构，应于本次专项整治结束前，提升安全防护和开发能力，确保系统能够满足保护客户资金信息安全、防止黑客攻击和系统中断等信息科技安全要求。</p>
